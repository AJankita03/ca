function createCalendar(month, year) {
    const firstDay = new Date(year, month, 1);
    const lastDate = new Date(year, month + 1, 0).getDate();

    calendarBody.innerHTML = '';

    monthYearDisplay.textContent = `${firstDay.toLocaleString('default', { month: 'long' })} ${year}`;

    // Blank spaces for the previous month's days
    for (let i = 0; i < firstDay.getDay(); i++) {
        const emptyDay = document.createElement('div');
        emptyDay.classList.add('empty');
        calendarBody.appendChild(emptyDay);
    }

    // Days of the current month
    for (let day = 1; day <= lastDate; day++) {
        const dayDiv = document.createElement('div');
        dayDiv.textContent = day;

        const event = events[firstDay.toLocaleString('default', { month: 'long' })]?.find(e => e.day === day);

        if (event) {
            dayDiv.setAttribute('data-title', event.title);
            dayDiv.setAttribute('data-description', event.description);
            dayDiv.setAttribute('data-image', event.image);
            dayDiv.classList.add('event-day');
            dayDiv.addEventListener('click', showEventInfo);
        }

        calendarBody.appendChild(dayDiv);
    }
}

function showEventInfo(e) {
    const title = e.target.getAttribute('data-title');
    const description = e.target.getAttribute('data-description');
    const image = e.target.getAttribute('data-image');

    modalTitle.textContent = title;
    modalDescription.textContent = description;
    modalImage.src = image;

    modal.style.display = 'flex';

    // Play the click sound
    clickSound.play();
}

function hideModal() {
    modal.style.display = 'none';
}

closeModal.addEventListener('click', hideModal);

// Close modal when clicking anywhere outside the modal
window.addEventListener('click', (event) => {
    if (event.target === modal) {
        hideModal();
    }
});

document.querySelector('.prev-month').addEventListener('click', () => {
    currentMonth--;
    if (currentMonth < 0) {
        currentMonth = 11;
        currentYear--;
    }
    createCalendar(currentMonth, currentYear);
});

document.querySelector('.next-month').addEventListener('click', () => {
    currentMonth++;
    if (currentMonth > 11) {
        currentMonth = 0;
        currentYear++;
    }
    createCalendar(currentMonth, currentYear);
});

// Search functionality
searchBar.addEventListener('input', (e) => {
    const query = e.target.value.toLowerCase();
    const days = document.querySelectorAll('.calendar-body div');

    days.forEach(day => {
        const eventTitle = day.getAttribute('data-title')?.toLowerCase() || '';
        if (eventTitle.includes(query)) {
            day.style.display = 'block';
        } else {
            day.style.display = 'none';
        }
    });
});

